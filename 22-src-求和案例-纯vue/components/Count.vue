<template>
  <div id="Count">
    <h4>当前求和为：{{ sum }}</h4>
    <select v-model.number="n">
      <option value="1">1</option>
      <option value="2">2</option>
      <option value="3">3</option>
    </select>
    <button @click="increment">+</button>
    <button @click="decrement">-</button>
    <button @click="incrementOdd">当前求和为奇数再加</button>
    <button @click="incrementWait">等一等再加</button>
  </div>
</template>

<script>
export default {
  name: 'MyCount',
  data() {
    return {
      n: 1,   // 用户选择的数据
      sum: 0, // 当前的和
    }
  },
  methods: {
    increment() {
      this.sum += this.n
    },
    decrement() {
      if (this.sum === 0) return alert('为 0 不能再减了')
      this.sum -= this.n
    },
    incrementOdd() {
      if (this.sum % 2) { this.sum += this.n }
    },
    incrementWait() {
      setTimeout(() => {
        this.sum += this.n
      }, 500)
    }
  }
}
</script>
